# Q2.Take Two numbers and print their - sum,difference,product,division and modulo

number_1 = int(input("enter the number: "))
number_2 = int(input("enter the number: "))

print("Sum of two numbers: ",number_1 + number_2)
print("Difference of two numbers: ",number_1 - number_2)
print("Product of two numbers: ",number_1 * number_2)
print("integer division of two numbers: ",number_1 // number_2)
print("modulo division of two numbers: ",number_1 % number_2)